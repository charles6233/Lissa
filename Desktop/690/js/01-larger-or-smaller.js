let num1 = parseInt(prompt("Enter a number :"))
let num2 = parseInt(prompt("Enter another number :"))
if (num1 > num2)    {
    document.write(num1 + " is larger than " + num2)
}else if (num1 < num2)    {
    document.write(num1 + " is smaller than " + num2)
}else    {
    document.write(num1 + " is equal to " + num2)
}




